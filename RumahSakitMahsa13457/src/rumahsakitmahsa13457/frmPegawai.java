    private void cmdHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdHapusActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPegawai.getSelectedRow();
        int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus item ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
    
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            // Mendapatkan nilai ID dari kolom yang sesuai
            String userId = (String) tblPegawai.getValueAt(selectedRow, 0);

            // Query SQL untuk menghapus data dari tabel
            String sql = "DELETE FROM user WHERE user_id='" + userId + "'";
            stm.executeUpdate(sql);

            // Memperbarui tampilan tabel setelah penghapusan
            baca_data();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
       }
    }//GEN-LAST:event_cmdHapusActionPerformed

