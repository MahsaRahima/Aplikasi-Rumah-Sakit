    private void cmdSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSimpanActionPerformed
        // TODO add your handling code here:
        String tId = txtId.getText();
        String tNama = txtNama.getText();
        String tAlamat = txtAlamat.getText();
        String tPassword = txtPassword.getText();

        try {
            if (edit == true){
                stm.executeUpdate("UPDATE user SET name='"+tNama+"', "+"alamat='"+tAlamat
                        +"', "+ "password='"+tPassword+ "' where user_id='" + tId + "'");

                //edit = false;
            }else{
                stm.executeUpdate("INSERT INTO user VALUES('"+NULL+"','"+tId+"','"+tNama+"','"+tAlamat
                    +"','"+tPassword+"')");
            }
            tblPegawai.setModel(new DefaultTableModel(dataTable, header));
            baca_data();
            aktif(false);
            setTombol(true);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Data belum dipilih");
        }
    }//GEN-LAST:event_cmdSimpanActionPerformed

