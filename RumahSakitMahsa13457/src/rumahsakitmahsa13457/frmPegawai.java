    private void baca_data(){
        try {
            stm = Con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_READ_ONLY);
            RsPegawai = stm.executeQuery("select * from user");

            ResultSetMetaData meta = RsPegawai.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
            while(RsPegawai.next()) {
                baris = RsPegawai.getRow();
        }
            dataTable = new Object[baris][col];
            int x = 0;
            RsPegawai.beforeFirst();
            while (RsPegawai.next()) {
                dataTable[x][0] = RsPegawai.getString("user_id");
                dataTable[x][1] = RsPegawai.getString("name");
                dataTable[x][2] = RsPegawai.getString("alamat");
                dataTable[x][3] = RsPegawai.getString("password");
                x++;
            }
            tblPegawai.setModel(new DefaultTableModel(dataTable,header));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
