    private void cmdSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSimpanActionPerformed
        // TODO add your handling code here:
        String tId = txtId.getText();
        String tNama = txtNama.getText();
        tgl=dtExp.getText();
        String tHarga = txtHarga.getText();
        String tStok = txtStok.getText();

        try {
            if (edit == true){
                stm.executeUpdate("UPDATE obat SET nm_obat='"+tNama
                    +"', satuan='"+sSatuan+"', expired='"+tgl
                    +"', hrg_obat='"+tHarga+"', stok='"+tStok+ "' where id_obat='" + tId + "'");

                //edit = false;
            }else{
                stm.executeUpdate("INSERT INTO obat VALUES('"+tId+"','"+tNama+"','"+sSatuan
                    +"','"+tgl+"','"+tHarga+"','"+tStok+"')");
            }
            tblObat.setModel(new DefaultTableModel(dataTable, header));
            baca_data();
            aktif(false);
            setTombol(true);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Data belum dipilih");
        }
    }//GEN-LAST:event_cmdSimpanActionPerformed

