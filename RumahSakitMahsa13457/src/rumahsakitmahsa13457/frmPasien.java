    private void cmdSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSimpanActionPerformed
        // TODO add your handling code here:
        String tId = txtId.getText();
        String tNama = txtNama.getText();
        String tAlamat = txtAlamat.getText();
        String tTelepon = txtTelepon.getText();
        String tAlergi = txtAlergi.getText();

        try {
            if (edit == true){
                stm.executeUpdate("UPDATE pasien SET nm_pasien='"+tNama
                    +"', "+"alm_pasien='"+tAlamat+"', "+ "phone='"+tTelepon
                    +"', jns_kelamin='"+sKelamin+"', goldar='"+sGoldar+"', "
                    +"alergi='"+tAlergi+ "' where id_pasien='" + tId + "'");

                //edit = false;
            }else{
                stm.executeUpdate("INSERT INTO pasien VALUES('"+tId+"','"+tNama+"','"+tAlamat
                    +"','"+tTelepon+"','"+sKelamin+"','"+sGoldar+"','"+ ""+tAlergi+"')");
            }
            tblPasien.setModel(new DefaultTableModel(dataTable, header));
            baca_data();
            aktif(false);
            setTombol(true);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Data belum dipilih");
        }
    }//GEN-LAST:event_cmdSimpanActionPerformed

