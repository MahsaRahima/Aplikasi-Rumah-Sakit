    private void baca_data(){
        try {
            stm = Con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_READ_ONLY);
            RsPasien = stm.executeQuery("select * from pasien");

            ResultSetMetaData meta = RsPasien.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
            while(RsPasien.next()) {
                baris = RsPasien.getRow();
        }
            dataTable = new Object[baris][col];
            int x = 0;
            RsPasien.beforeFirst();
            while (RsPasien.next()) {
                dataTable[x][0] = RsPasien.getString("id_pasien");
                dataTable[x][1] = RsPasien.getString("nm_pasien");
                dataTable[x][2] = RsPasien.getString("alm_pasien");
                dataTable[x][3] = RsPasien.getString("phone");
                dataTable[x][4] = RsPasien.getString("jns_kelamin");
                dataTable[x][5] = RsPasien.getString("goldar");
                dataTable[x][6] = RsPasien.getString("alergi");
                x++;
            }
            tblPasien.setModel(new DefaultTableModel(dataTable,header));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
