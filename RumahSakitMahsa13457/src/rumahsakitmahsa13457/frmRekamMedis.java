    private void baca_data(){
        try {
            stm = Con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_READ_ONLY);
            RsRekam = stm.executeQuery("SELECT * from v_rekmedis;");

            ResultSetMetaData meta = RsRekam.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
            while(RsRekam.next()) {
                baris = RsRekam.getRow();
        }
            dataTable = new Object[baris][col];
            int x = 0;
            RsRekam.beforeFirst();
            while (RsRekam.next()) {
                dataTable[x][0] = RsRekam.getString("id_rekmedis");
                dataTable[x][1] = RsRekam.getString("nm_pasien");
                dataTable[x][2] = RsRekam.getString("nm_dokter");
                dataTable[x][3] = RsRekam.getString("tgl_periksa");
                dataTable[x][4] = RsRekam.getString("diagnosa");
                x++;
            }
            tblRekam.setModel(new DefaultTableModel(dataTable,header));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
