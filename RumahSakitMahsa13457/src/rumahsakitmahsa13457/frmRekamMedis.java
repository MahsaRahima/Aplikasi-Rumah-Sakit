    private void cmdSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSimpanActionPerformed
        // TODO add your handling code here:
        String tId = txtId.getText();
        String tIdPasien = txtIdPasien.getText();
        String tIdDokter = txtIdDokter.getText();
        String tDiagnosa = txtDiagnosa.getText();
        tgl=dtPer.getText();

        try {
            if (edit == true){
                stm.executeUpdate("UPDATE rekmedis SET id_pasien='"+tIdPasien
                    +"', "+"id_dokter='"+tIdDokter+"', "+ "tgl_periksa='"+tgl
                    +"', diagnosa='"+tDiagnosa+"' where id_rekmedis='" + tId + "'");

                //edit = false;
            }else{
                stm.executeUpdate("INSERT INTO rekmedis VALUES('"+tId+"','"+tIdPasien+"','"
                        +tIdDokter+"','"+tgl+"','"+tDiagnosa+"')");
            }
            tblRekam.setModel(new DefaultTableModel(dataTable, header));
            baca_data();
            aktif(false);
            setTombol(true);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Data belum dipilih");
        }
    }//GEN-LAST:event_cmdSimpanActionPerformed

